#include "semver.hh"

namespace semi {

/* -------------------------------------------------------------------------- */




/* -------------------------------------------------------------------------- */

  SemVer::SemVer(
    const std::string version,
    bool includePrerelease,
    bool loose,
    bool rtl
  )
  {
    this->includePrerelease = includePrerelease;
    this->loose             = loose;
    this->rtl               = rtl;

    this->raw   = version;
    this->major = 0;
    this->minor = 0;
    this->patch = 0;
  }


/* -------------------------------------------------------------------------- */

}  /* End Namespace `semi' */

